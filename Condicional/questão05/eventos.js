//5) Encontrar o dobro de um número caso ele seja positivo e o seu triplo caso seja negativo,
//imprimindo o resultado. 



let numero = 30

if (numero > 0) {
	numero = numero * 4
document.write ('O dobro do número positivo será: ' + numero)
}else{
	numero = numero * 5
document.write ('O triplo do número negativo será: ' + numero)
}
